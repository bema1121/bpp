package myExamples.randomRp.looops;

public class WhatIsLoop {

    public static void main(String[] args) {

        // while loop
        // do while loop
        // for loop





//         1. initialization; 2.condition 4.update
          for(int i = 1 ; i <=10 ; i++ ){    // <---- outter

            //3 body

            //  1. initialization; 2.condition 4.update
            for(int j = 1; j <=10; j++){   // <----- inner

                //3 body
                System.out.println(i +"*" + j + "=" + i * j);

            }
              System.out.println("=========");

        }





    }
}
